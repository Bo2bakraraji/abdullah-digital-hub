const documentRoot = document.documentElement;
const header = document.querySelector("[data-header]");
const menuToggle = document.querySelector("[data-menu-toggle]");
const menu = document.querySelector("[data-menu]");
const languageToggle = document.querySelector("[data-language-toggle]");
const consentBanner = document.querySelector("[data-consent]");

function storageGet(key) {
  try { return localStorage.getItem(key); } catch { return null; }
}

function storageSet(key, value) {
  try { localStorage.setItem(key, value); } catch { /* Storage can be disabled. */ }
}

function setLanguage(language) {
  const selected = translations[language] ? language : "en";
  documentRoot.lang = selected;
  documentRoot.dir = selected === "ar" ? "rtl" : "ltr";

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    const value = translations[selected][key];
    if (!value) return;
    if (key === "hero.title") element.innerHTML = value;
    else element.textContent = value;
  });

  if (languageToggle) {
    languageToggle.textContent = selected === "ar" ? "English" : "العربية";
    languageToggle.setAttribute("aria-label", selected === "ar" ? "Switch to English" : "التبديل إلى العربية");
  }
  storageSet("adh-language", selected);
}

if (languageToggle) {
  setLanguage(storageGet("adh-language") || "en");
  languageToggle.addEventListener("click", () => setLanguage(documentRoot.lang === "ar" ? "en" : "ar"));
}

function closeMenu() {
  if (!menu || !menuToggle) return;
  menu.classList.remove("open");
  menuToggle.setAttribute("aria-expanded", "false");
}

if (menuToggle && menu) {
  menuToggle.addEventListener("click", () => {
    const open = !menu.classList.contains("open");
    menu.classList.toggle("open", open);
    menuToggle.setAttribute("aria-expanded", String(open));
  });
  menu.querySelectorAll("a").forEach((link) => link.addEventListener("click", closeMenu));
  window.addEventListener("resize", () => { if (window.innerWidth > 820) closeMenu(); });
}

function updateHeader() {
  if (header) header.classList.toggle("scrolled", window.scrollY > 24);
}
updateHeader();
window.addEventListener("scroll", updateHeader, { passive: true });

const prefersReducedMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;
const reveals = document.querySelectorAll(".reveal");
if (prefersReducedMotion || !("IntersectionObserver" in window)) {
  reveals.forEach((element) => element.classList.add("visible"));
} else {
  const revealObserver = new IntersectionObserver((entries, observer) => {
    entries.forEach((entry) => {
      if (!entry.isIntersecting) return;
      entry.target.classList.add("visible");
      observer.unobserve(entry.target);
    });
  }, { threshold: 0.12 });
  reveals.forEach((element) => revealObserver.observe(element));
}

let trackingLoaded = false;

function loadGoogleAnalytics() {
  window.dataLayer = window.dataLayer || [];
  window.gtag = function gtag() { window.dataLayer.push(arguments); };
  window.gtag("js", new Date());
  window.gtag("config", "G-5G493681LP", { anonymize_ip: true });
  const script = document.createElement("script");
  script.async = true;
  script.src = "https://www.googletagmanager.com/gtag/js?id=G-5G493681LP";
  document.head.appendChild(script);
}

function loadMetaPixel() {
  /* Meta's standard queue lets events be registered before the library finishes loading. */
  const fbq = window.fbq = function () {
    fbq.callMethod ? fbq.callMethod.apply(fbq, arguments) : fbq.queue.push(arguments);
  };
  if (window._fbq) return;
  window._fbq = fbq;
  fbq.push = fbq;
  fbq.loaded = true;
  fbq.version = "2.0";
  fbq.queue = [];
  const script = document.createElement("script");
  script.async = true;
  script.src = "https://connect.facebook.net/en_US/fbevents.js";
  document.head.appendChild(script);
  fbq("init", "2031547784111351");
  fbq("track", "PageView");
}

function loadTikTokPixel() {
  const ttq = window.ttq = window.ttq || [];
  ttq.methods = ["page", "track", "identify", "instances", "debug", "on", "off", "once", "ready", "alias", "group", "enableCookie", "disableCookie", "holdConsent", "revokeConsent", "grantConsent"];
  ttq.setAndDefer = (target, method) => { target[method] = function () { target.push([method].concat(Array.from(arguments))); }; };
  ttq.methods.forEach((method) => ttq.setAndDefer(ttq, method));
  ttq.instance = (id) => {
    const instance = ttq._i?.[id] || [];
    ttq.methods.forEach((method) => ttq.setAndDefer(instance, method));
    return instance;
  };
  ttq.load = (id, options = {}) => {
    const source = "https://analytics.tiktok.com/i18n/pixel/events.js";
    ttq._i = ttq._i || {};
    ttq._i[id] = [];
    ttq._i[id]._u = source;
    ttq._t = ttq._t || {};
    ttq._t[id] = Date.now();
    ttq._o = ttq._o || {};
    ttq._o[id] = options;
    const script = document.createElement("script");
    script.async = true;
    script.src = `${source}?sdkid=${id}&lib=ttq`;
    const firstScript = document.getElementsByTagName("script")[0];
    firstScript.parentNode.insertBefore(script, firstScript);
  };
  ttq.load("D9301BBC77U0LQ3AESQ0");
  ttq.page();
}

function loadTracking() {
  if (trackingLoaded) return;
  trackingLoaded = true;
  loadGoogleAnalytics();
  loadMetaPixel();
  loadTikTokPixel();
}

function recordInteraction(label) {
  const isLead = label.startsWith("whatsapp");
  if (typeof window.gtag === "function") {
    window.gtag("event", isLead ? "generate_lead" : "outbound_click", { method: label });
  }
  if (isLead && typeof window.fbq === "function") window.fbq("track", "Contact", { method: "WhatsApp" });
  if (isLead && window.ttq && typeof window.ttq.track === "function") window.ttq.track("Contact", { method: "WhatsApp" });
}

document.querySelectorAll("[data-track]").forEach((element) => {
  element.addEventListener("click", () => recordInteraction(element.dataset.track));
});

const consentChoice = storageGet("adh-consent");
if (consentChoice === "accepted") loadTracking();
else if (!consentChoice && consentBanner) window.setTimeout(() => { consentBanner.hidden = false; }, 500);

document.querySelector("[data-consent-accept]")?.addEventListener("click", () => {
  storageSet("adh-consent", "accepted");
  consentBanner.hidden = true;
  loadTracking();
});

document.querySelector("[data-consent-reject]")?.addEventListener("click", () => {
  storageSet("adh-consent", "necessary");
  consentBanner.hidden = true;
});
